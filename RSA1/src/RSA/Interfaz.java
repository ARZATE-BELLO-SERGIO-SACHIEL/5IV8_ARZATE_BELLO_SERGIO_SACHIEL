    boolean verificar(String numero, String tamanio){
        String expresion_regular = "^[0-9]+$";
        if(tamanio.equals("") || numero.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor llene los campos");
        }
        if(!numero.matches(expresion_regular) || !numero.matches(expresion_regular)){
            JOptionPane.showMessageDialog(null, "Por favor ingresar solo numeros");
            return false;
        }
        int num = Integer.parseInt(tamanio);
        if(!(num >= 1 && num <= 3)){
           JOptionPane.showMessageDialog(null, "La longitud de los numeros primos debe de ser entre 1 y 3");
           return false;
        }
        
        return true;
    }
