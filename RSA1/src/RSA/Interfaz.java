    private void BCifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCifrarActionPerformed
        // TODO add your handling code here:
        String mensaje = Numero.getText();
        String tamanio = Tamano.getText();
        int num, lon_bits = 0;
        if(verificar(mensaje, tamanio)){
            num = Integer.parseInt(tamanio);
            
            switch(num){
                case 1:
                    lon_bits = 3;
                    break;
                case 2:
                    lon_bits = 7;
                    break;
                case 3:
                    lon_bits = 10;
                    break;
            }
            RSA Cifrar = new RSA(lon_bits);
            Cifrar.generarPrimos();
            Cifrar.generarClaves();
            BigInteger[] res_cifrar = Cifrar.cifrar(mensaje);
            String descifrado = Cifrar.descifrar(res_cifrar);
            Decifrado.setText(descifrado);
            String cifrado = "";
            for(int i = 0; i < res_cifrar.length; ++i){
                cifrado += res_cifrar[i];
                cifrado += "\n";
            }
            Cifrado.setText(cifrado);
        }
    }//GEN-LAST:event_BCifrarActionPerformed

